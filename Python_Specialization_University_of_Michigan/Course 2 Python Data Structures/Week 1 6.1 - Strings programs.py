# =============================================================================
# Slicing Strings
# =============================================================================
s = 'Monty Python'
print(s[0:4])
print(s[6:7])
print(s[6:20])

# Output
# Mont
# P
# Python


# We can look through any continuous section of a string using the
# colon operator ' : '
# second number is upto but not including > print(s[6:7])
# if second number is beyond the end, loop stops at the end > print(s[6:20])

# =============================================================================
# Slicing Strings 2
# =============================================================================
s = 'Monty Python'
print(s[:2])
print(s[8:])
print(s[:])
# if you leave the spot open, it assumes to start from the beginning or
# stop at the end

# Output
# Mo
# thon
# Monty Python
