# =============================================================================
# Looping through Strings (WHILE LOOP)
# =============================================================================
fruit = 'banana'
index = 0
while index < len(fruit):
    letter = fruit[index]
    print(index, letter)
    index = index + 1

# Output
# 0 b
# 1 a
# 2 n
# 3 a
# 4 n
# 5 a

# =============================================================================
