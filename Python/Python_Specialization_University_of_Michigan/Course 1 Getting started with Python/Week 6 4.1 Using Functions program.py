# def is the keyword that we use to define a function. Function is like 
# a variable. Except it holds code, instead of a value. If a function is 
# called/invoked, then the code inside the function is executed. Otherwise, 
# the function's code are not executed, just stored 

def thing():
    print('Hello')
    print('Fun')

thing()

print('Zip')

thing()

# Output
# Hello
# Fun
# Zip
# Hello
# Fun

